        <h1 class="page-header">Research Paper MLA Format</h1>

        <article class="loop-1 post-198 page type-page status-publish hentry">
          <div class="tag-meta">
          </div>
          <p>If your instructor has specific requirements for the format of your research paper, check them before preparing your final draft. When you submit your paper, be sure to keep a secure copy.</p>

          <p>The most common formatting is presented in the sections below:</p>
          <ul>
            <li><a href="#margins">Margins</a></li>
            <li><a href="#textformatting">Text Formatting</a></li>
            <li><a href="#headingandtitle">Heading and Title</a></li>
