            <figure id="attachment_331" style="width: 550px" class="wp-caption alignnone"><img class="size-full wp-image-331" src="./Formatting a Research Paper – The MLA Style Center_files/fig1.png" alt="Fig. 1. The top of the first page of a research paper." width="550" height="275" srcset="https://style.mla.org/files/2016/03/fig1.png 550w, https://style.mla.org/files/2016/03/fig1-300x150.png 300w" sizes="(max-width: 550px) 100vw, 550px"><figcaption class="wp-caption-text"><strong>Fig. 1.</strong> The top of the first page of a research paper.</figcaption></figure>

            <p>A research paper does not need a title page. If your teacher requires a title page in lieu of or in addition to the header, format it according to the instructions you are given.</p>
          </div>
          <a name="runningheadpagenumbers"><h2>Running Head with Page Numbers</h2></a>
          <p>Number all pages consecutively throughout the research paper in the upper right-hand corner, half an inch from the top and flush with the right margin. Type your last name, followed by a space, before the page number (<a href="https://style.mla.org/formatting-papers/#attachment_111">fig. 2</a>). Do not use the abbreviation <em>p.</em> before the page number or add a period, a hyphen, or any other mark or symbol. Your writing program will probably allow you to create a running head of this kind that appears automatically on every page. Some teachers prefer that no running head appear on the first page. Follow your teacher’s preference.</p>
          <div class="figure">

            <figure id="attachment_111" style="width: 550px" class="wp-caption alignnone"><img class="size-full wp-image-111" src="./Formatting a Research Paper – The MLA Style Center_files/formatting_fig2.png" alt="Fig. 2. The running head of a research paper." width="550" height="125"><figcaption class="wp-caption-text"><strong>Fig. 2.</strong> The running head of a research paper.</figcaption></figure>

          </div>
          <a name="placementworkscited"><h2>Placement of the List of Works Cited</h2></a>
          <p>The list of works cited appears at the end of the paper, after any endnotes. Begin the list on a new page. The list contains the same running head as the main text. The page numbering in the running head continues uninterrupted throughout. For example, if the text of your research paper (including any endnotes) ends on page 10, the works-cited list begins on page 11. Center the title, <em>Works Cited</em>, an inch from the top of the page (<a href="https://style.mla.org/formatting-papers/#attachment_405">fig. 3</a>). (If the list contains only one entry, make the heading <em>Work Cited</em>.) Double-space between the title and the first entry. Begin each entry flush with the left margin; if an entry runs more than one line, indent the subsequent line or lines half an inch from the left margin. This format is sometimes called <em>hanging indention</em>, and you can set your writing program to create it automatically for a group of paragraphs. Hanging indention makes alphabetical lists easier to use. Double-space the entire list. Continue it on as many pages as necessary.</p>
          <div class="figure">

            <figure id="attachment_405" style="width: 550px" class="wp-caption alignnone"><img class="size-full wp-image-405" src="./Formatting a Research Paper – The MLA Style Center_files/fig_3.png" alt="Fig. 3. The top of the first page of a works-cited list." width="550" height="309" srcset="https://style.mla.org/files/2016/03/fig_3.png 550w, https://style.mla.org/files/2016/03/fig_3-300x169.png 300w" sizes="(max-width: 550px) 100vw, 550px"><figcaption class="wp-caption-text"><strong>Fig. 3.</strong> The top of the first page of a works-cited list.</figcaption></figure>

          </div>
          <a name="tablesandillustrations"><h2>Tables and Illustrations</h2></a>
          <p>Place tables and illustrations as close as possible to the parts of the text to which they relate. A table is usually labeled <em>Table</em>, given an arabic numeral, and titled. Type both label and title flush left on separate lines above the table, and capitalize them as titles (do not use all capital letters). Give the source of the table and any notes immediately below the table in a caption. To avoid confusion between notes to the text and notes to the table, designate notes to the table with lowercase letters rather than with numerals. Double-space throughout; use dividing lines as needed (<a href="https://style.mla.org/formatting-papers/#attachment_406">fig. 4</a>).</p>
          <div class="figure">

            <figure id="attachment_406" style="width: 550px" class="wp-caption alignnone"><img class="size-full wp-image-406" src="./Formatting a Research Paper – The MLA Style Center_files/fig_4.png" alt="Fig. 4. A table in a research paper." width="550" height="708" srcset="https://style.mla.org/files/2016/03/fig_4.png 550w, https://style.mla.org/files/2016/03/fig_4-233x300.png 233w" sizes="(max-width: 550px) 100vw, 550px"><figcaption class="wp-caption-text"><strong>Fig. 4.</strong> A table in a research paper.</figcaption></figure>

          </div>
          <p>Any other type of illustrative visual material—for example, a photograph, map, line drawing, graph, or chart—should be labeled <em>Figure</em> (usually abbreviated <em>Fig.</em>), assigned an arabic numeral, and given a caption: “Fig. 1. Mary Cassatt, <em>Mother and Child</em>, Wichita Art Museum.” A label and caption ordinarily appear directly below the illustration and have the same one-inch margins as the text of the paper (<a href="https://style.mla.org/formatting-papers/#attachment_407">fig. 5</a>). If the caption of a table or illustration provides complete information about the source and the source is not cited in the text, no entry for the source in the works-cited list is necessary.</p>
          <div class="figure">

            <figure id="attachment_407" style="width: 550px" class="wp-caption alignnone"><img class="size-full wp-image-407" src="./Formatting a Research Paper – The MLA Style Center_files/fig_5.png" alt="Fig. 5. A figure in a research paper." width="550" height="414" srcset="https://style.mla.org/files/2016/03/fig_5.png 550w, https://style.mla.org/files/2016/03/fig_5-300x226.png 300w" sizes="(max-width: 550px) 100vw, 550px"><figcaption class="wp-caption-text"><strong>Fig. 5.</strong> A figure in a research paper.</figcaption></figure>

          </div>
          Musical illustrations are labeled <em>Example</em> (usually abbreviated <em>Ex.</em>), assigned an arabic numeral, and given a caption: “Ex. 1. Pyotr Ilich Tchaikovsky, Symphony no. 6 in B, opus 74 (<em>Pathétique</em>), finale.” A label and caption ordinarily appear directly below the example and have the same one-inch margins as the text of the paper (<a href="https://style.mla.org/formatting-papers/#attachment_115">fig. 6</a>).
