            <li><a href="#placementworkscited">Placement of the List of Works Cited</a></li>
            <li><a href="#tablesandillustrations">Tables and Illustrations</a></li>
            <li><a href="#paperandprinting">Paper and Printing</a></li>
            <li><a href="#correctionsandinsertions">Corrections and Insertions on Printouts</a></li>
            <li><a href="#bindingpaper">Binding a Printed Paper</a></li>
            <li><a href="#electronicsubmission">Electronic Submission</a></li>
          </ul>
          <a name="margins"><h2>Margins</h2></a>
          <p>Except for the running head (see below), leave margins of one inch at the top and bottom and on both sides of the text. If you plan to submit a printout on paper larger than 8½ by 11 inches, do not print the text in an area greater than 6½ by 9 inches.</p>
          <a name="textformatting"><h2>Text Formatting</h2></a>
          <p>Always choose an easily readable typeface (e.g., Times New Roman) in which the regular type style contrasts clearly with the italic, and set it to a standard size (e.g., 12 points). Do not justify the lines of text at the right margin; turn off any automatic hyphenation feature in your writing program. Double-space the entire research paper, including quotations, notes, and the list of works cited. Indent the first line of a paragraph half an inch from the left margin. Indent set-off quotations half an inch as well (for examples, see&nbsp;76–80 in the <em>MLA Handbook</em>). Leave one space after a period or other concluding punctuation mark, unless your instructor prefers two spaces.</p>
          <a name="headingandtitle"><h2>Heading and Title</h2></a>
          Beginning one inch from the top of the first page and flush with the left margin, type your name, your instructor’s name, the course number, and the date on separate lines, double-spacing the lines. On a new, double-spaced line, center the title (<a href="https://style.mla.org/formatting-papers/#attachment_331">fig. 1</a>). Do not italicize or underline your title, put it in quotation marks or boldface, or type it in all capital letters. Follow the rules for capitalization in the <em>MLA Handbook</em> (67–68), and italicize only the words that you would italicize in the text.
          <blockquote class="example">
            <p class="extract">Local Television Coverage of International News Events</p>
            <p class="extract">The Attitude toward Violence in <em>A Clockwork Orange</em></p>
            <p class="extract">The Use of the Words <em>Fair</em> and <em>Foul</em> in Shakespeare’s <em>Macbeth</em></p>
            <p class="extract">Romanticism in England and the <em>Scapigliatura</em> in Italy</p>
          </blockquote>
          <p>Do not use a period after your title or after any heading in the paper (e.g., <em>Works Cited</em>). Begin your text on a new, double-spaced line after the title, indenting the first line of the paragraph half an inch from the left margin.</p>

