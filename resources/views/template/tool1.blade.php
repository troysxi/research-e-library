          </div>
          <p>Paperpile is a web-based commercial reference management software, with special emphasis on integration with Google Docs and Google Scholar. Parts of Paperpile are implemented as a Google Chrome browser extension. It was founded in 2012, and is produced by Paperpile LLC.</p>

          <a name=""><h2>Get organized with Paperpile's smart and simple interface</h2></a>
          <p>Organize your papers with folders, labels and stars. Search
            your library in real-time or quickly look up a paper
            online.</p>
            <p>Automatically fix references with incomplete data and clean
              up duplicates. Always keep your library organized and
              clean. Effortlessly.
            </p>
            <a name="#"><h2>Functionality and features</h2></a>
            <p>Paperpile imports data from academic publisher websites and from databases such as PubMed, Google Scholar, Google Books, and arXiv. Paperpile can retrieve and store publication PDF files to the user's Google Drive account. It formats citations and bibliographies in Google Docs,[6] which allows collaborative editing of academic papers.</p>
            <p>Paperpile imports and exports BibTeX and RIS formats, and can migrate data from Mendeley, Zotero and Papers.</p>


            <a name="#"><h2>Technology</h2></a>
            <p>Paperpile is a web application combined with a browser extension for Google Chrome making it accessible to users on Windows, Linux, macOS, as well as Chrome OS platforms. It is built using HTML5 and Javascript as well as several Javascript libraries such as jQuery and Ext JS. Paperpile is available for install at the Google Chrome web store. Version updates of Paperpile are managed automatically by Google Chrome.</p>



