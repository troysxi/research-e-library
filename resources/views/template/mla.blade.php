          </div>
          <a name="paperandprinting"><h2>Paper and Printing</h2></a>
          <p>If you print your paper, use only white, 8½-by-11-inch paper of good quality. If you lack 8½-by-11-inch paper, choose the closest size available. Use a high-quality printer. Some instructors prefer papers printed on a single side because they’re easier to read, but others allow printing on both sides as a means of conserving paper; follow your instructor’s preference.</p>
          <a name="correctionsandinsertions"><h2>Corrections and Insertions on Printouts</h2></a>
          <p>Proofread and correct your research paper carefully before submitting it. If you are checking a printout and find a mistake, reopen the document, make the appropriate revisions, and reprint the corrected page or pages. Be sure to save the changed file. Spelling checkers and usage checkers are helpful when used with caution. They do not find all errors and sometimes label correct material as erroneous. If your instructor permits corrections on the printout, write them neatly and legibly in ink directly above the lines involved, using carets (⁁) to indicate where they go. Do not use the margins or write a change below the line it affects. If corrections on any page are numerous or substantial, revise your document and reprint the page.</p>
          <a name="bindingpaper"><h2>Binding a Printed Paper</h2></a>
          <p>Pages of a printed research paper may get misplaced or lost if they are left unattached or merely folded down at a corner. Although a plastic folder or some other kind of binder may seem an attractive finishing touch, most instructors find such devices a nuisance in reading and commenting on students’ work. Many prefer that a paper be secured with a simple paper or binder clip, which can be easily removed and restored. Others prefer the use of staples.</p>
          <a name="electronicsubmission"><h2>Electronic Submission</h2></a>
          <p>There are at present no commonly accepted standards for the electronic submission of research papers. If you are asked to submit your paper electronically, obtain from your teacher guidelines for formatting, mode of submission (e.g., by e-mail, on a Web site), and so forth and follow them closely.</p>
